    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        boolean check = false;
        int i;
        for (i = 1; i <= adminList.getSize() && (!check);) {
            if (jTextField1.getText().equals(adminList.getEntry(i).getId())) {
                check = true;
            }
            i++;
        }
        if (check) {
            enableChange();
            displayOriData(adminList.getEntry(i - 1));
        } else {
            JOptionPane.showMessageDialog(null, "The Admin ID does not exist", "ERROR", JOptionPane.ERROR_MESSAGE);
            jTextField1.grabFocus();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

