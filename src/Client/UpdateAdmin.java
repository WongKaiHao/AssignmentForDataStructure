    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        boolean check = false;
        String id = jTextField1.getText();
        String name = jTextField2.getText();
        String pass = jTextField3.getText();
        if (jTextField3.getText().equals(jTextField4.getText())) {
            Iterator adminLoop = adminList.getIterator();
            while (adminLoop.hasNext() && !check) {
                Admin admin = (Admin) adminLoop.next();
                if (admin.getId().equals(jTextField1.getText())) {
                    admin.updateAdmin(new Admin(id,name,pass));
                    check = true;
                }
            }
            if (check) {
                JOptionPane.showInternalMessageDialog(null, "Modification done\n", "INFO", JOptionPane.INFORMATION_MESSAGE);
                writeAdminList();
                dispose();
                new AdminManagement().setVisible(true);
            } else {
                JOptionPane.showInternalMessageDialog(null, "Modification FAILED \n Please try again", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showInternalMessageDialog(null, "Password confirmation is not matched ! ! !\n", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private boolean validatePassword(String password) {
        boolean check = false;
        if (8 <= password.length() && password.length() <= 10) {
            if (password.matches(".*\\d.*") && password.matches(".*[A-Z].*") && password.matches(".*[a-z].*")) {
                check = true;
            }
        }
        return check;
    }

    private void enableChange() {
        jTextField2.setEditable(true);
        jTextField3.setEditable(true);
        jTextField4.setEditable(true);
    }

    private void displayOriData(Admin admin) {
        jTextField2.setText("" + admin.getName());
        jTextField3.setText("" + admin.getPassword());
    }

